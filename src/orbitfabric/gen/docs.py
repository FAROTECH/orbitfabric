def _faults_by_source(model: MissionModel) -> dict[str, list[Fault]]:
    grouped: dict[str, list[Fault]] = {}

    for fault in sorted(model.faults, key=lambda item: (item.source, item.id)):
        grouped.setdefault(fault.source, []).append(fault)

