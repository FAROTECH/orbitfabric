from __future__ import annotations

from pathlib import Path
from typing import Any

from orbitfabric.model.mission import (
    Command,
    CommandArgument,
    Event,
    Fault,
    FaultCondition,
    FaultRecovery,
    MissionModel,
    Packet,
    PayloadContract,
    QualityPolicy,
    TelemetryItem,
    TelemetryLimits,
)


def generate_markdown_docs(model: MissionModel, output_dir: Path) -> list[Path]:
    """Generate Markdown mission reference documentation."""
    output_dir.mkdir(parents=True, exist_ok=True)

    generated_files = [
        _write(output_dir / "telemetry.md", _render_telemetry(model)),
        _write(output_dir / "commands.md", _render_commands(model)),
        _write(output_dir / "events.md", _render_events(model)),
        _write(output_dir / "faults.md", _render_faults(model)),
        _write(output_dir / "modes.md", _render_modes(model)),
        _write(output_dir / "packets.md", _render_packets(model)),
    ]

    if model.payloads:
        generated_files.append(
            _write(output_dir / "payloads.md", _render_payloads(model))
        )

    return generated_files


def _write(path: Path, content: str) -> Path:
    path.write_text(content, encoding="utf-8")
    return path


def _header(title: str, model: MissionModel) -> str:
    return (
        f"# {title}\n\n"
        f"Mission: `{model.spacecraft.id}`  \n"
        f"Model version: `{model.spacecraft.model_version}`  \n"
        "Generated by OrbitFabric. Do not edit manually.\n\n"
        "This page is generated from the validated Mission Model.\n\n"
    )


def _render_telemetry(model: MissionModel) -> str:
    lines = [_header("Telemetry Reference", model)]

    lines.append("## Summary\n\n")
    lines.append(f"- Telemetry items: `{len(model.telemetry)}`\n")
    lines.append(f"- Source subsystems: `{len({item.source for item in model.telemetry})}`\n\n")

    lines.append("## Telemetry by subsystem\n\n")

    for source, items in _telemetry_by_source(model).items():
        lines.append(f"### {_subsystem_heading(model, source)}\n\n")
        lines.append(
            "| ID | Name | Type | Unit | Sampling | Criticality | "
            "Persistence | Downlink | Limits | Quality |\n"
        )
        lines.append("|---|---|---|---|---|---|---|---|---|---|\n")

        for item in items:
            lines.append(_render_telemetry_row(item))

        lines.append("\n")

    return "".join(lines)


def _render_telemetry_row(item: TelemetryItem) -> str:
    return (
        f"| {_code(item.id)} "
        f"| {_text(item.name)} "
        f"| {_code(item.type)} "
        f"| {_code(item.unit)} "
        f"| {_code(item.sampling)} "
        f"| {_code(item.criticality)} "
        f"| {_code(item.persistence)} "
        f"| {_code(item.downlink_priority)} "
        f"| {_format_limits(item.limits)} "
        f"| {_format_quality(item.quality)} |\n"
    )


def _render_commands(model: MissionModel) -> str:
    lines = [_header("Command Reference", model)]

    lines.append("## Summary\n\n")
    lines.append(f"- Commands: `{len(model.commands)}`\n")
    target_count = len({command.target for command in model.commands})
    lines.append(f"- Target subsystems: `{target_count}`\n\n")

    lines.append("## Commands by target subsystem\n\n")

    for target, commands in _commands_by_target(model).items():
        lines.append(f"### {_subsystem_heading(model, target)}\n\n")
        lines.append(
            "| ID | Risk | Allowed Modes | Timeout | ACK | Arguments | "
            "Emits | Expected Effects |\n"
        )
        lines.append("|---|---|---|---:|---|---|---|---|\n")

        for command in commands:
            lines.append(_render_command_row(command))

        lines.append("\n")

    return "".join(lines)


def _render_command_row(command: Command) -> str:
    return (
        f"| {_code(command.id)} "
        f"| {_code(command.risk)} "
        f"| {_format_list(command.allowed_modes)} "
        f"| {_format_timeout(command.timeout_ms)} "
        f"| {_format_bool(command.requires_ack)} "
        f"| {_format_arguments(command.arguments)} "
        f"| {_format_list(command.emits)} "
        f"| {_format_expected_effects(command.expected_effects)} |\n"
    )


