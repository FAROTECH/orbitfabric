def _subsystem_heading(model: MissionModel, subsystem_id: str) -> str:
    subsystems = {subsystem.id: subsystem for subsystem in model.subsystems}
    subsystem = subsystems.get(subsystem_id)

    if subsystem is None:
        return _code(subsystem_id)

    return f"{_code(subsystem_id)} - {_text(subsystem.name)}"


def _format_arguments(arguments: list[CommandArgument]) -> str:
    if not arguments:
        return "-"

    return _line_break_join(_format_argument(argument) for argument in arguments)


def _format_argument(argument: CommandArgument) -> str:
    details = [f"{_code(argument.name)}: {_code(argument.type)}"]

    constraints: list[str] = []
    if argument.min is not None:
        constraints.append(f"min {_code(argument.min)}")
    if argument.max is not None:
        constraints.append(f"max {_code(argument.max)}")
    if argument.enum:
        constraints.append(f"enum {_format_list(argument.enum)}")
    if argument.default is not None:
        constraints.append(f"default {_code(argument.default)}")

    if constraints:
        details.append(f"({', '.join(constraints)})")

    if argument.description:
        details.append(f"- {_text(argument.description)}")

    return " ".join(details)


def _format_expected_effects(expected_effects: dict[str, Any]) -> str:
    if not expected_effects:
        return "-"

    if expected_effects.get("no_state_change") is True:
        return "No state change"

    lines: list[str] = []

    payload_lifecycle = expected_effects.get("payload_lifecycle")
    if isinstance(payload_lifecycle, dict):
        payload_id = payload_lifecycle.get("payload")
        state = payload_lifecycle.get("state")
        if payload_id is not None and state is not None:
            lines.append(f"payload {_code(payload_id)} lifecycle -> {_code(state)}")

    telemetry_effects = expected_effects.get("telemetry", {})
    if isinstance(telemetry_effects, dict):
        for telemetry_id, value in sorted(telemetry_effects.items()):
            lines.append(f"{_code(telemetry_id)} = {_code(value)}")

    mode_transition = expected_effects.get("mode_transition")
    if isinstance(mode_transition, dict):
        target_mode = mode_transition.get("to")
        if target_mode is not None:
            lines.append(f"mode -> {_code(target_mode)}")

    ignored_keys = {
        "telemetry",
        "mode_transition",
        "no_state_change",
        "payload_lifecycle",
    }
    remaining = {
        key: value
        for key, value in expected_effects.items()
        if key not in ignored_keys
    }

    if remaining:
        lines.extend(_format_mapping_lines(remaining))

    return _line_break_join(lines)


def _format_fault_condition(condition: FaultCondition) -> str:
    lines: list[str] = []

    if condition.telemetry is not None:
        operator = condition.operator or "?"
        value = _code(condition.value) if condition.value is not None else "`?`"
        lines.append(f"{_code(condition.telemetry)} {operator} {value}")

    if condition.event is not None:
        lines.append(f"event {_code(condition.event)}")

    if condition.debounce_samples is not None:
        lines.append(f"debounce {_code(condition.debounce_samples)} samples")

    if condition.occurrences is not None:
        lines.append(f"occurrences {_code(condition.occurrences)}")

    if condition.window_s is not None:
        lines.append(f"window {_code(condition.window_s)} s")

    return _line_break_join(lines) if lines else "-"


def _format_recovery(recovery: FaultRecovery | None) -> str:
    if recovery is None:
        return "-"

    lines: list[str] = []

    if recovery.mode_transition is not None:
        lines.append(f"mode -> {_code(recovery.mode_transition)}")

    if recovery.auto_commands:
        lines.append(f"auto commands: {_format_list(recovery.auto_commands)}")

    return _line_break_join(lines) if lines else "-"


def _format_limits(limits: TelemetryLimits | None) -> str:
    if limits is None:
        return "-"

    values = limits.model_dump(exclude_none=True)
    if not values:
        return "-"

    return _line_break_join(
        f"{_code(key)} = {_code(value)}"
        for key, value in sorted(values.items())
    )


def _format_quality(quality: QualityPolicy | None) -> str:
    if quality is None:
        return "-"

    values = quality.model_dump(exclude_none=True)
    if not values:
        return "-"

    return _line_break_join(
        f"{_code(key)} = {_code(value)}"
        for key, value in sorted(values.items())
    )


def _format_timeout(timeout_ms: int | None) -> str:
    if timeout_ms is None:
        return "-"

    return str(timeout_ms)


def _format_list(values: list[str]) -> str:
    if not values:
        return "-"

    return ", ".join(_code(value) for value in values)


def _format_mapping_lines(mapping: dict[str, Any]) -> list[str]:
    lines: list[str] = []

    for key, value in sorted(mapping.items()):
        if isinstance(value, dict):
            nested = ", ".join(
                f"{nested_key}={_format_plain_value(nested_value)}"
                for nested_key, nested_value in sorted(value.items())
            )
            lines.append(f"{_code(key)}: {nested}")
        elif isinstance(value, list):
            lines.append(f"{_code(key)}: {_format_list([str(item) for item in value])}")
        else:
            lines.append(f"{_code(key)} = {_code(value)}")

    return lines


def _line_break_join(values: Any) -> str:
    items = [item for item in values if item]
    if not items:
        return "-"

    return "<br>".join(items)


def _format_bool(value: bool) -> str:
    return "true" if value else "false"


def _format_plain_value(value: Any) -> str:
    if isinstance(value, bool):
        return _format_bool(value)

    if value is None:
        return "null"

    return str(value)


def _code(value: Any) -> str:
    return f"`{_text(_format_plain_value(value))}`"


def _text(value: str) -> str:
    return str(value).replace("\n", " ").replace("|", "\\|")