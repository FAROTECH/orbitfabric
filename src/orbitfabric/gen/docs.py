from __future__ import annotations

import json
from pathlib import Path
from typing import Any

from orbitfabric.model.mission import (
    Command,
    Event,
    Fault,
    MissionModel,
    Packet,
    TelemetryItem,
)


def generate_markdown_docs(model: MissionModel, output_dir: Path) -> list[Path]:
    """Generate Markdown mission reference documentation."""
    output_dir.mkdir(parents=True, exist_ok=True)

    generated_files = [
        _write(output_dir / "telemetry.md", _render_telemetry(model)),
        _write(output_dir / "commands.md", _render_commands(model)),
        _write(output_dir / "events.md", _render_events(model)),
        _write(output_dir / "faults.md", _render_faults(model)),
        _write(output_dir / "modes.md", _render_modes(model)),
        _write(output_dir / "packets.md", _render_packets(model)),
    ]

    return generated_files


def _write(path: Path, content: str) -> Path:
    path.write_text(content, encoding="utf-8")
    return path


def _header(title: str, model: MissionModel) -> str:
    return (
        f"# {title}\n\n"
        f"Mission: `{model.spacecraft.id}`  \n"
        f"Model version: `{model.spacecraft.model_version}`  \n"
        "Generated by OrbitFabric. Do not edit manually.\n\n"
    )


def _render_telemetry(model: MissionModel) -> str:
    lines = [_header("Telemetry Reference", model)]

    lines.append(
        "| ID | Name | Type | Unit | Source | Sampling | Criticality | "
        "Persistence | Downlink | Limits | Quality |\n"
    )
    lines.append("|---|---|---:|---:|---|---|---|---|---|---|---|\n")

    for item in model.telemetry:
        lines.append(_render_telemetry_row(item))

    return "".join(lines)


def _render_telemetry_row(item: TelemetryItem) -> str:
    return (
        f"| `{item.id}` "
        f"| {item.name} "
        f"| `{item.type}` "
        f"| `{item.unit}` "
        f"| `{item.source}` "
        f"| `{item.sampling}` "
        f"| `{item.criticality}` "
        f"| `{item.persistence}` "
        f"| `{item.downlink_priority}` "
        f"| {_format_model(item.limits)} "
        f"| {_format_model(item.quality)} |\n"
    )


def _render_commands(model: MissionModel) -> str:
    lines = [_header("Command Reference", model)]

    lines.append(
        "| ID | Target | Risk | Allowed Modes | Timeout ms | Requires ACK | "
        "Arguments | Emits | Expected Effects |\n"
    )
    lines.append("|---|---|---|---|---:|---:|---|---|---|\n")

    for command in model.commands:
        lines.append(_render_command_row(command))

    return "".join(lines)


def _render_command_row(command: Command) -> str:
    arguments = ", ".join(
        f"`{arg.name}: {arg.type}`" for arg in command.arguments
    ) or "-"
    emits = _format_list(command.emits)
    allowed_modes = _format_list(command.allowed_modes)

    return (
        f"| `{command.id}` "
        f"| `{command.target}` "
        f"| `{command.risk}` "
        f"| {allowed_modes} "
        f"| {command.timeout_ms if command.timeout_ms is not None else '-'} "
        f"| {str(command.requires_ack).lower()} "
        f"| {arguments} "
        f"| {emits} "
        f"| {_format_mapping(command.expected_effects)} |\n"
    )


def _render_events(model: MissionModel) -> str:
    lines = [_header("Event Reference", model)]

    lines.append("| ID | Source | Severity | Downlink | Persistence | Description |\n")
    lines.append("|---|---|---|---|---|---|\n")

    for event in model.events:
        lines.append(_render_event_row(event))

    return "".join(lines)


def _render_event_row(event: Event) -> str:
    return (
        f"| `{event.id}` "
        f"| `{event.source}` "
        f"| `{event.severity}` "
        f"| `{event.downlink_priority or '-'}` "
        f"| `{event.persistence or '-'}` "
        f"| {event.description} |\n"
    )


def _render_faults(model: MissionModel) -> str:
    lines = [_header("Fault Reference", model)]

    lines.append(
        "| ID | Source | Severity | Condition | Emits | Recovery Mode | "
        "Auto Commands |\n"
    )
    lines.append("|---|---|---|---|---|---|---|\n")

    for fault in model.faults:
        lines.append(_render_fault_row(fault))

    return "".join(lines)


def _render_fault_row(fault: Fault) -> str:
    recovery_mode = "-"
    auto_commands = "-"

    if fault.recovery is not None:
        recovery_mode = fault.recovery.mode_transition or "-"
        auto_commands = _format_list(fault.recovery.auto_commands)

    return (
        f"| `{fault.id}` "
        f"| `{fault.source}` "
        f"| `{fault.severity}` "
        f"| {_format_model(fault.condition)} "
        f"| {_format_list(fault.emits)} "
        f"| `{recovery_mode}` "
        f"| {auto_commands} |\n"
    )


def _render_modes(model: MissionModel) -> str:
    lines = [_header("Mode Reference", model)]

    lines.append("## Modes\n\n")
    lines.append("| Mode | Initial | Description |\n")
    lines.append("|---|---:|---|\n")

    for mode_id, mode in model.modes.items():
        lines.append(
            f"| `{mode_id}` | {str(mode.initial).lower()} | {mode.description} |\n"
        )

    lines.append("\n## Mode Transitions\n\n")
    lines.append("| From | To | Reason | Description |\n")
    lines.append("|---|---|---|---|\n")

    for transition in model.mode_transitions:
        description = transition.description or "-"
        lines.append(
            f"| `{transition.from_mode}` "
            f"| `{transition.to}` "
            f"| `{transition.reason}` "
            f"| {description} |\n"
        )

    return "".join(lines)


def _render_packets(model: MissionModel) -> str:
    lines = [_header("Packet Reference", model)]

    lines.append("| ID | Name | Type | Max Payload Bytes | Period | Telemetry |\n")
    lines.append("|---|---|---|---:|---|---|\n")

    for packet in model.packets:
        lines.append(_render_packet_row(packet))

    return "".join(lines)


def _render_packet_row(packet: Packet) -> str:
    return (
        f"| `{packet.id}` "
        f"| {packet.name} "
        f"| `{packet.type}` "
        f"| {packet.max_payload_bytes} "
        f"| `{packet.period or '-'}` "
        f"| {_format_list(packet.telemetry)} |\n"
    )


def _format_list(values: list[str]) -> str:
    if not values:
        return "-"
    return ", ".join(f"`{value}`" for value in values)


def _format_model(value: Any) -> str:
    if value is None:
        return "-"

    if hasattr(value, "model_dump"):
        return _format_mapping(value.model_dump(exclude_none=True))

    return _format_mapping(value)


def _format_mapping(value: Any) -> str:
    if value is None:
        return "-"

    if value == {}:
        return "-"

    return "`" + json.dumps(value, sort_keys=True) + "`"