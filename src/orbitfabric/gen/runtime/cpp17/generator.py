def _render_data_product_registry(elements: tuple[RuntimeElement, ...]) -> str:
    rows = [
        "    {"
        f"DataProductId::{element.symbol_name}, "
        f"{_cpp_string(element.model_id)}, "
        f"{_cpp_string(element.symbol_name)}, "
        f"{_cpp_string(_metadata_string(element, 'producer'))}, "
        f"{_cpp_string(_metadata_string(element, 'type'))}, "
        f"{_cpp_string(_metadata_string(element, 'priority'))}"
        "},"
        for element in elements
    ]
    return _render_registry("DataProductDescriptor", "DataProductRegistry", rows)


def _render_registry(descriptor: str, registry_name: str, rows: list[str]) -> str:
    lines = [
        f"inline constexpr std::array<{descriptor}, {len(rows)}> {registry_name} = {{{{\n"
    ]
    lines.extend(f"{row}\n" for row in rows)
    lines.append("}};\n\n")
    return "".join(lines)


def _render_enum(name: str, elements: tuple[RuntimeElement, ...]) -> str:
    lines = [f"enum class {name} : std::uint16_t {{\n"]
    lines.append("    Invalid = 0,\n")

    for element in elements:
        lines.append(f"    {element.symbol_name} = {element.numeric_id},\n")

    lines.append("};\n\n")
    return "".join(lines)


def _enum_symbol(value: str) -> str:
    parts = [part for part in value.replace("-", "_").split("_") if part]
    if not parts:
        return "Unknown"
    return "".join(part[:1].upper() + part[1:] for part in parts)


def _metadata_string(element: RuntimeElement, key: str) -> str:
    value = element.metadata.get(key)
    if value is None:
        return ""
    return str(value)


def _cpp_type(argument: RuntimeArgument) -> str:
    return _CPP_TYPE_MAP.get(argument.value_type.lower(), "const char*")


def _field_name(model_id: str) -> str:
    cleaned = re.sub(r"[^A-Za-z0-9_]", "_", model_id)
    cleaned = re.sub(r"_+", "_", cleaned).strip("_")

    if not cleaned:
        return "unnamed"

    if cleaned[0].isdigit():
        return f"arg_{cleaned}"

    return cleaned


def _cpp_string(value: str) -> str:
    escaped = value.replace("\\", "\\\\").replace('"', '\\"')
    return f'"{escaped}"'


def _cpp_bool(value: bool) -> str:
    return "true" if value else "false"
