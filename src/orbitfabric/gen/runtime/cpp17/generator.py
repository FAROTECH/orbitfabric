from __future__ import annotations

from pathlib import Path

from orbitfabric.gen.runtime.contract import RuntimeContract, RuntimeElement

_HEADER = """// Generated by OrbitFabric. Do not edit manually.
// Source of truth: Mission Model.
// This file contains runtime-facing contract identifiers only.
// It is not flight software and contains no onboard behavior.

#pragma once

#include <cstdint>

namespace orbitfabric::generated {

"""

_FOOTER = """}  // namespace orbitfabric::generated
"""


def generate_cpp17_runtime_files(contract: RuntimeContract, output_dir: Path) -> list[Path]:
    """Generate the initial C++17 runtime-facing identifier headers."""
    include_dir = output_dir / contract.generation_profile / "include" / "orbitfabric" / "generated"
    include_dir.mkdir(parents=True, exist_ok=True)

    generated_files = [
        _write(include_dir / "mission_ids.hpp", _render_mission_ids(contract)),
        _write(include_dir / "mission_enums.hpp", _render_mission_enums(contract)),
    ]

    return generated_files


def _write(path: Path, content: str) -> Path:
    path.write_text(content, encoding="utf-8")
    return path


def _render_mission_ids(contract: RuntimeContract) -> str:
    lines = [_HEADER]
    lines.append(_render_enum("ModeId", contract.modes))
    lines.append(_render_enum("TelemetryId", contract.telemetry))
    lines.append(_render_enum("CommandId", contract.commands))
    lines.append(_render_enum("EventId", contract.events))
    lines.append(_render_enum("FaultId", contract.faults))
    lines.append(_render_enum("PacketId", contract.packets))
    lines.append(_render_enum("PayloadId", contract.payloads))
    lines.append(_render_enum("DataProductId", contract.data_products))
    lines.append(_render_enum("StoragePolicyId", contract.storage_policies))
    lines.append(_render_enum("DownlinkPolicyId", contract.downlink_policies))
    lines.append(_FOOTER)
    return "".join(lines)


def _render_mission_enums(contract: RuntimeContract) -> str:
    lines = [_HEADER]
    lines.append("enum class RuntimeValueType : std::uint16_t {\n")
    lines.append("    Invalid = 0,\n")

    value_types = sorted(
        {
            str(item.metadata["type"])
            for item in contract.telemetry
            if "type" in item.metadata
        }
        | {
            argument.value_type
            for command in contract.commands
            for argument in command.arguments
        }
    )

    for index, value_type in enumerate(value_types, start=1):
        lines.append(f"    {_enum_symbol(value_type)} = {index},\n")

    lines.append("};\n\n")
    lines.append(_FOOTER)
    return "".join(lines)


def _render_enum(name: str, elements: tuple[RuntimeElement, ...]) -> str:
    lines = [f"enum class {name} : std::uint16_t {{\n"]
    lines.append("    Invalid = 0,\n")

    for element in elements:
        lines.append(f"    {element.symbol_name} = {element.numeric_id},\n")

    lines.append("};\n\n")
    return "".join(lines)


def _enum_symbol(value: str) -> str:
    parts = [part for part in value.replace("-", "_").split("_") if part]
    if not parts:
        return "Unknown"
    return "".join(part[:1].upper() + part[1:] for part in parts)
